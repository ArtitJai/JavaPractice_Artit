package day12_NestedLoop;

public class EmptyString {
	static String msg ;
	public static void main(String[] args) {
	
		String word = "";
		String word2 = new String();
		
		System.out.println(word.length()); // count word in string => 0
		System.out.println(word2.length());// count word in string => 0
		
	
		System.out.println(msg);// Null
		
		System.out.println(msg.length()); // String out of main Empty string
	} 

}
