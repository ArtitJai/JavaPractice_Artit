package day07_2ndReview;

public class RelationalDemo {

	public static void main(String[] args) {
		 
		// Relational Operator = Boolean expression
		/*
		 * 1. >
		 * 2. <
		 * 3. >=
		 * 4. <=
		 * 5. == means equal
		 * 6. != means not equal
		 */
		
		System.out.println(10 != 4); // true
		char ch1 = 'a';
		char ch2 = 'b';
		System.out.println(ch1 == ch2); // false
		
		
		
		
	}

}
