package day31_8thReview;

public class WrapperClass {

	public static void main(String[] args) {
		
        // Wrapper class
        // Byte, Short, Integer, Long, Float, Double, Character, Boolean
        
        // Why do we need?
        // Collection, cannot store primitive data type. (only object)
        
        byte a1 = 3; // initialize primitive
        
        // auto boxing/ wrap
        // converting primitive value to wrapper class
        Byte a = a1;
        
        // Unboxing / Unwrap
        // converting wrapper class to primitive
        
        Integer b1 = 3;
        int b2 = b1; 
        
        

	}

}
