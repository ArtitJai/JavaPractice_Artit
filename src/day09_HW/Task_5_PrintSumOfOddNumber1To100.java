package day09_HW;

public class Task_5_PrintSumOfOddNumber1To100 {

	public static void main(String[] args) {
		
		//Write a java program to print sum of odd number between 1 to 100.
	
	
		int num = 0;
		
		for(int i = 1 ; i <= 100; i++) {
			if (i % 2 == 1){
				num += i;
				// num = num + i;
			}
			
			
		}
	
		System.out.println("SUM = "+num);
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	}

}
